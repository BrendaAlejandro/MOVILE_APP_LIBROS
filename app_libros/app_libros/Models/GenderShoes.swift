import Foundation

struct GenderShoes: Identifiable, Decodable {
    let id: Int
    let name: String
    let brand: String
    let gender: String
    let category: String
    let price: Int32
    let image: String
    var isFavorite: Bool = false
    
    enum CodingKeys: String, CodingKey {
        case id = "id"
        case name = "name"
        case brand = "brand"
        case gender = "gender"
        case category = "category"
        case image = "image"
        case price = "price"
    }
}


